define(function(require, exports, module)
{
    'use strict';

	var lib = require('glympse-adapter/lib/utils');


	// Exported class
	function MemberInvite(data, cfg)
	{
		// state

		// consts
		var dbg = lib.dbg('MemberInvite', cfg.dbg);

		// TODO: Just map data props directly??
		//	---> Only want immediate non-Objects/Arrays
		var props = [ 'locale'
					, 'type'
					, 'send'
					, 'invite_code'
					, 'address'
					];



		///////////////////////////////////////////////////////////////////////////////
		// PROPERTIES
		///////////////////////////////////////////////////////////////////////////////

		// NOTE: some properties created via lib.mapProps

		this.getData = function()
		{
			return data;
		};

		this.setData = function(val)
		{
			data = val;
			lib.mapProps(this, props, data);
		};


		///////////////////////////////////////////////////////////////////////////////
		// PUBLICS
		///////////////////////////////////////////////////////////////////////////////

		this.toJSON = function()
		{
			return data;
		};


		///////////////////////////////////////////////////////////////////////////////
		// UTILITY
		///////////////////////////////////////////////////////////////////////////////


		///////////////////////////////////////////////////////////////////////////////
		// CTOR
		///////////////////////////////////////////////////////////////////////////////

		this.setData(data);
	}

	// MemberInvite defines


	module.exports = MemberInvite;
});
